"""
+: SOMA, -:SUBTRAÇÂO, *:MULTIPLICAÇÂO, /:DIVISÂO, //DIVISÂO INTEIRA, **:POTENCIAÇÂO, %:RESTO DA DIVISÃO,():MUDA A ORDEM DAS OPERAÇÕES
"""
print('Multiplicação de 10 * 3 =',10*3)
print('Soma de 10 + 3 =',10+3)
print('Subtração de 10 - 3 =',10-3)
print('Divisão de 10 / 3 =',10/3)
print('Resto de 10 % 3 =',10%3)
print('Divisão inteira de 10 // 3 =',10//3)
print('Potência de 10 ** 3 =',10**3)

